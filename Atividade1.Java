public class Atividade1 {
    public static void main(String[] args) {

 int i;

 for (i = 1; i <= 50; i++) {


     if (i % 3 == 0 && i % 3 == 0 ){
         System.out.println(i + " FizzBuzz");
     }

     else if (i % 3 == 0 ){
         System.out.println(i + " Fizz");
     }

     else if (i % 5 == 0){
         System.out.println(i + " Buzz");
     }

    else{
         System.out.println(i);
     }

        }
    }
}
